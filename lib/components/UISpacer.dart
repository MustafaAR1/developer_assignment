import 'package:flutter/material.dart';

class UISpacer {
  static Widget spacerH20({double height = 20}) {
    return SizedBox(height: height);
  }

  static Widget spacerH16({double height = 16}) {
    return SizedBox(height: height);
  }

  static Widget spacerH24({double height = 24}) {
    return SizedBox(height: height);
  }

  static Widget spacerW24({double width = 24}) {
    return SizedBox(width: width);
  }
}
